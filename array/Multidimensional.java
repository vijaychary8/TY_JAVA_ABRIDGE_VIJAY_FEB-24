package com.capgemini.assignment.array;

public class Multidimensional {
	 public static void main(String[] args) 
	    { 
	  
	        int[][] arr = { { 1, 2 ,6,3 }, { 3, 4 ,8, 9 } }; 
	  
	        for (int i = 0; i < 2; i++) { 
	            for (int j = 0; j < 4; j++) { 
	                System.out.print(arr[i][j] + " "); 
	            } 
	  
	            System.out.println(); 
	        } 
	    
	    }

}
